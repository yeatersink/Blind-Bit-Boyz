<h1>Wallet Tracker</h1>
<p>So you don't have a life at all and you want to be nosey and track someones wallet? Maybe you are a good person, but you want to try to get the jump on your favorite influencer? Maybe you want to try to find out which wallet holds the most coins before you ape in? Thats a good idea.</p>    
<p>This tool will allow you to track a contract address to find out the following:</p> 
<ol> 
1 Search for a contract address.
2 Track the Wallet which owns the coin searched.
</ol>
<p>To use this tool, simply put the contract address of the coin that you want to track. Then press the search button.</p>
<p>After the results appear, just click on the name of the coin and a new tab will open with a list of the wallett addresses which currently own that coin. </p>
<p>To see other coins owned by that wallet, simply click on the wallet address, and a new tab will open which shows you a list of coins owned by that wallet.</p>
